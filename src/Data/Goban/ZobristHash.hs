-- type Hash = Int64
type ZHash = Int
type VState = (Vertex, VertexState)
type ZHashMap = M.Map VState ZHash



initMap :: Int -> IO ZHashMap
initMap n =
    foldM f M.empty xs
    where
      xs = [(v, s) | v <- vs, s <- [Colored Black, Colored White, EmptyKoBlocked]]
      vs = allVertices n

      f :: ZHashMap -> VState -> IO ZHashMap
      f m x = do
        y <- randomIO
        return $ M.insert x y m
          
updateHash :: ZHash -> VState -> ZHash
updateHash h v =
    h `xor` (fromJust $ M.lookup v m)
    where
      m = zMap


